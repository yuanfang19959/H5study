// \d 代表数字
// \D 非数字
// \w 数字字母下划线
// \W 除\w
// \s 匹配空格（换行符 制表符 空格符等）
// \S 非空格的字符
// | 或者

// 例子：两种格式 010-12345678 或者 0530-1234567
reg = /^010-\d{8}|0530-\d{7}$/
reg = /^(010|0530)-\d{7,8}$/

log(reg.test('0530-650710188'))
log(reg.test('010-6507101221'))


reg = /^[12345]{3}/
log(reg.test('123'))